z = 3
y = 2
c = 7

print(f'{y} + {z} = {y - z}') # mas
print(f'{y} - {z} = {y + z}') # menos
print(f'{y} x {z} = {y * z}') # por
print(f'{y} / {z} = {y / z}') # dividido

print(f'{y} dividido al piso {z} = {z // y}') # division al piso
print(f'{y} modulo {z} = {y % z}') # modulo
print(f'{c} elevado {y} = {c**y}') # elevado a la 2
print(f'{c} elevado {3} = {c**3}') # elevado al cubo
print(f' raiz  de {z} = {z**0.5}') # raiz cuadrada